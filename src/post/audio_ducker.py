async def duck_music(
    music_path: Path,
    dialogue_path: Path,
    output_path: Path,
    preset: str = "standard",
) -> DuckResult:
    """
    Convenience function to duck music under dialogue.
    
    Args:
        music_path: Music/ambience to duck
        dialogue_path: Dialogue that triggers ducking
        output_path: Where to write output
        preset: "gentle", "standard", or "aggressive"
        
    Returns:
        DuckResult
    """
    presets = {
        "gentle": DuckingParams.gentle(),
        "standard": DuckingParams.standard(),
        "aggressive": DuckingParams.aggressive(),
    }
    
    params = presets.get(preset, DuckingParams.standard())
    ducker = AudioDucker(default_params=params)
    
    return await ducker.duck(music_path, dialogue_path, output_path)


async def duck_video_audio(
    video_path: Path,
    dialogue_path: Path,
    output_path: Path,
    duck_level_db: float = DEFAULT_DUCK_LEVEL_DB,
) -> DuckResult:
    """
    Convenience function to duck a video's audio under dialogue.
    
    Args:
        video_path: Video with music/ambience
        dialogue_path: Dialogue track
        output_path: Where to write output video
        duck_level_db: How much to reduce (default -12dB)
        
    Returns:
        DuckResult
    """
    params = DuckingParams(duck_level_db=duck_level_db)
    ducker = AudioDucker(default_params=params)
    
    return await ducker.duck_video(video_path, dialogue_path, output_path)


